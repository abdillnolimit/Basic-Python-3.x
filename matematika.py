#Modul Sederhana
def tambah(*args) ->int :
    for i in args :
        hasil = 0
    return i + hasil

def kali(*args) ->int :
    for i in args :
        hasil = 0
    return i * hasil


def eksponen(n) ->float :
    return lambda angka:angka**n
